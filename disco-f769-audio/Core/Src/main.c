void wm8994_init_codec(uint32_t sampleRate) {

	printf("\r\nHello STM32F769ZI-Disco\r\n");

	// set audio frequency
	hsai_BlockA1.Init.AudioFrequency = sampleRate;
	if (HAL_SAI_InitProtocol(&hsai_BlockA1, SAI_I2S_STANDARD, SAI_PROTOCOL_DATASIZE_16BIT, 2) != HAL_OK)
	{
	  Error_Handler();
	}

	// start DMA
	HAL_SAI_Transmit_DMA(&hsai_BlockA1, (uint8_t*) PlaybackBuffer, AUDIO_BUFFER_SIZE);
	HAL_SAI_Receive_DMA(&hsai_BlockB1, (uint8_t*) RecordBuffer, AUDIO_BUFFER_SIZE);


	// config audio codec
	if (wm8994_ReadID(AUDIO_I2C_ADDRESS) == WM8994_ID) {

		printf("Found WM8994 audio codec\r\n");

		// audio codec reset
		wm8994_Reset(AUDIO_I2C_ADDRESS);

		// audio codec init
		wm8994_Init(AUDIO_I2C_ADDRESS, INPUT_DEVICE_INPUT_LINE_1 | OUTPUT_DEVICE_HEADPHONE, 64, AUDIO_FREQUENCY_44K);

		// audio codec unmute
		wm8994_SetMute(AUDIO_I2C_ADDRESS, AUDIO_MUTE_OFF);

		printf("Running at %ld samples/sec...\r\n", sampleRate);

		// enable LED
		HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, 1);
	}
}

int _write(int fd, char * ptr, int len) {

  HAL_UART_Transmit(&huart1, (uint8_t *) ptr, len, HAL_MAX_DELAY);
  return len;
}

